package java240812;

/*
 * 인터페이스로 규격화된 코드를 만들 수 있습니다.
 * 인터페이스에는 소스를 구현하는게 아닌 선언만 할 수 있습니다.
 * Java에서는 'interface'라는 키워드를 사용하여 클래스를 선언하듯이 사용합니다. (다형성이라 합니다.)
 * 
 * 객체지향언어 특징
 * 캡슐화 => 클래스에는 필드변수, 메소드, 생성자
 * 상속 => 부모클래스 상속
 * 다형성 => 인터페이스로 구현 후 클래스에서 '재정의'(재사용 의미는 같은거 같음)
 */
public interface PhoneInterface {
      public void 결제하다();//선언만 가능함.
      public void 운영체제조회();//인터페이스에서 구현된 메소드는 전부 무조건 'public'이다
      
}
